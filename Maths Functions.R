# max() and min() can be used to find the highest and lowest numbers in a set respectively.
# You can use numeric and integer number types but not complex.
max(5, 10.5, 3L)
min(5, 10.5, 3L)

# sqrt() returns the square root of a number
sqrt(6.25)

# abs() returns the absolute value of a number
abs(-2.9)

# ceiling() rounds upwards to the nearest integer.
# floor() rounds downwards to the nearest integer.

ceiling(4.1)
floor(4.1)